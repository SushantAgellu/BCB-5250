def parse_fasta(data):
    
    sequences = {}
    lines = data.strip().split("\n")
    
    label = ""
    for line in lines:
        if line.startswith(">"):
            label = line[1:]  
            sequences[label] = ""  
        else:
            sequences[label] += line  
    
    return sequences

def overlap_graph(sequences, k=3):
    
    edges = []
    
    for s1 in sequences:
        suffix = sequences[s1][-k:] 
        for s2 in sequences:
            if s1 != s2 and sequences[s2].startswith(suffix):  
                edges.append((s1, s2))
    
    return edges


