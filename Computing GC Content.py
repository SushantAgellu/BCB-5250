def gc_content(dna):
   
    return (dna.count('G') + dna.count('C')) / len(dna) * 100

def highest_gc_content(fasta_data):
   
    fasta_data = fasta_data.strip().split('>')
    sequences = {seq.split('\n', 1)[0]: seq.split('\n', 1)[1].replace('\n', '') for seq in fasta_data if seq}
    
    max_label = max(sequences, key=lambda k: gc_content(sequences[k]))
    return max_label, gc_content(sequences[max_label])

